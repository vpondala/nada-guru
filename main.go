func main() {
	mode := flag.String("mode", "cli", "run mode: cli | server")
	flag.Parse()

	// Configure structured logging based on LOG_FORMAT env var.
	logFormat := os.Getenv("LOG_FORMAT")
	var handler slog.Handler
	if logFormat == "json" {
		handler = slog.NewJSONHandler(os.Stderr, nil)
	} else {
		handler = slog.NewTextHandler(os.Stderr, nil)
	}
	slog.SetDefault(slog.New(handler))

	fmt.Print(banner)

	switch *mode {
	case "cli":
		if err := cli.Run(); err != nil {
			slog.Error("CLI exited with error", "error", err)
			os.Exit(1)
		}
	case "server":
		if err := server.Start(); err != nil {
			slog.Error("Server exited with error", "error", err)
			os.Exit(1)
		}
	default:
		fmt.Fprintf(os.Stderr, "unknown mode %q ‚ÄĒ use --mode=cli or --mode=server\n", *mode)
		os.Exit(2)
	}
}
